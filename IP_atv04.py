#Repetição com for
for i in range(1,21):
  if (i != 13):
    print(i)

#Repetição com while
i = 0

while i < 20:
  i += 1
  if (i != 13):
    print(i)

#Desafio com ordem reversa - for
for i in range(20,0,-1):
  if (i != 13):
    print(i)
    
#Desafio com ordem reversa - while
i = 20

while i > 0:
  if (i != 13):
    print(i)
  i -= 1
